def format_legal_response(raw_text: str, source_label: str = "Groq") -> str:
    """Format an AI-generated legal response for Telegram."""
    if not raw_text or not raw_text.strip():
        return "❌ Sorry, I could not generate a response. Please try again."

    text = raw_text.strip()

    if source_label == "Gemini":
        badge = "📄 *Document Analysis* (via Gemini AI)\n\n"
    else:
        badge = "⚖️ *Legal Analysis* (via VoltLegal AI)\n\n"

    return badge + text + DISCLAIMER + QUICK_ACTIONS


