def format_draft_response(raw_text: str) -> str:
    """Format a generated legal draft document."""
    if not raw_text or not raw_text.strip():
        return "❌ Could not generate the draft. Please try again with more details."

    text = raw_text.strip()
    header = "📄 *Legal Document Draft*\n\n"
    footer = (
        "\n\n⚠️ *Important:*\n"
        "_This is a DRAFT document generated by AI. "
        "Please review it carefully and consult a lawyer before using it officially. "
        "Verify all details, dates, and legal references._"
    )
    return header + text + footer


